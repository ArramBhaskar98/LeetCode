class Solution:
    def countBits(self, n: int):

        #My Method
        ans = []
        for i in range(n+1):
            st = bin(i)[2:]
            ans.append(st.count("1"))
        return ans

        # Short Method
        ans = [0]
        for i in range(1, n+1):
            ans.append(ans[i//2]+i%2)
        return ans

